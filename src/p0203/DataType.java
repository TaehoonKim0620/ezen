package p0203;

public class DataType {
	
	public static void main(String[] args) {
		// 기본형 참조형
		// 기본형 : 숫자, 문자, 논리
		// 숫자 : 정수, 실수
		// 정수
		// 첫글자가 대문자이면 참조형, 그렇지 않으면 기본형
		byte b = 1;
		short s;
		int i;
		long l;
		// 실수
		float f = (float) 1.1;
		float f2 = 1.1F;
		double db;
		//문자
		char c;
		// 논리
		boolean bl;
	}
}
